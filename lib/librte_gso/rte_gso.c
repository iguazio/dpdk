#include <errno.h>

#include "rte_gso.h"

int
rte_gso_segment(struct rte_mbuf *pkt,
		const struct rte_gso_ctx *gso_ctx,
		struct rte_mbuf **pkts_out,
		uint16_t nb_pkts_out)
{
	if (pkt == NULL || pkts_out == NULL || gso_ctx == NULL ||
			nb_pkts_out < 1)
		return -EINVAL;

	pkt->ol_flags &= (~PKT_TX_TCP_SEG);
	pkts_out[0] = pkt;

	return 1;
}
